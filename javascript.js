function Calculate(nums) {
  let i = 0,
    j = 1,
    sum = 0,
    capacity = 0,
    cap;
  while (j < nums.length) {
    if (nums[i] <= nums[j]) {
      sum = 0;
      for (let c = i + 1; c < j; c++) sum = sum + (nums[i] - nums[c]);

      cap = nums[i] * (j - i) + sum;
    }
    if (capacity < cap) {
      capacity = cap;
      index1 = i;
      index2 = j;
      i = j;
      j = i + 1;
      continue;
    }
    j++;
  }
  return [index1, index2, capacity];
}
